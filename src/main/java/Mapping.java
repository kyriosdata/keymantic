
public class Mapping 
{
	private String keyword;
	private String archetypeTerm;
	
	public Mapping(String keyword, String archetypeTerm) {
		super();
		this.keyword = keyword;
		this.archetypeTerm = archetypeTerm;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public String getArchetypeTerm() {
		return archetypeTerm;
	}

	public void setArchetypeTerm(String archetypeTerm) {
		this.archetypeTerm = archetypeTerm;
	}
	
	
	
	
}
